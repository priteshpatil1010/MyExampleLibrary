package com.herba.sdk.patillib;

public class Patil {

    public int add (int a, int b)
    {
        return a+b;
    }

    public int subs (int a, int b)
    {
        return a-b;
    }

    public int multi (int a, int b)
    {
        return a*b;
    }

    public int div (int a, int b)
    {
        return a/b;
    }

    public int getStringLength(String s)
    {
        return s.length();
    }
}
